//What is an undefined? - An undefined value in JavaScript is a value that has not been assigned a value. It is not the same as null, which is a special value that represents the absence of a value. Undefined values are often used to indicate that a variable has not been declared or that a function does not return a value.

// Here are some examples of how undefined can be used in JavaScript:

// Declare a variable without assigning a value
var myVariable;

// Check if a variable is undefined
if (myVariable === undefined) {
  // Do something
}

// Assign an undefined value to a variable
myVariable = undefined;

// Return an undefined value from a function
function myFunction() {
  return;
}
