//What is a variable in JavaScript? - This is a container that stores values

// Variables are declared using the following Javascript Keywords - let, var and const. This three words are used in different occasions. For Instance if you want to define a variable that you are going to change the values later, it is advised that you either use VAR or LET. If your variable value is not going to change you can use CONST.

let car = "BMW";

var bike = "SUZUKI";

const stack = "MERN Stack";

//In the above examples we have declared and assigned values to variables. To read the values always call the variable name as below.

console.log(car);
console.log(bike);
console.log(stack);

//What are the rules for naming variables in javaScript?
// * The variable name must start with a letter or the underscore character or a dollar sign
// * The variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )
// * Variable names are case-sensitive (age, Age and AGE are three different variables)
// * You cannot use keywords as your variable names
// * Variables do not need to be declared before you assign them a value
//Variable names cannot contain spaces

//Here are some of the valid variable names:
// * `name`
// * `age`
// * `_score`
// * `$total`

//Here are some of the invalid variable names:
// * `1name` (starts with a number)
// * `my name` (contains a space)
// * `function` (reserved word)

// NB
// It is important to choose variable names that are descriptive and easy to understand. This will make your code more readable and maintainable.
