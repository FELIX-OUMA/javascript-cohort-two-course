//What is a boolean? - A boolean is a data type that can have only two values: true or false. Booleans are often used to represent logical statements, such as whether a user is logged in or whether a form has been submitted.

//Booleans can be created using the following syntax:

let boo1 = true;
let boo2 = false;

console.log(boo1);
console.log(boo2);

//Booleans can be manipulated using a variety of methods, including:

//toString() - this will convert a boolean to a string

console.log(boo1.toString());

//valueOf() - reaturns the primitive value of a boolean

console.log(boo1.valueOf());

//N/B - Booleans are a fundamental data type in JavaScript and are used extensively in web development.
